def read_netstring(s):
    size = 0
    while True:
        byte = s.recv(1)
        if byte == b':':
            break
        size = size * 10 + int(byte)
    msg = b""
    while size != 0:
        msg += s.recv(size)
        size -= len(msg)
    byte = s.recv(1)
    assert byte == b','
    return msg.decode("utf-8")

